const LEVELS: ReadonlyArray<readonly [CefrLevel, string[]]> = [
    ['C2', C2_WORDS],
    ['C1', C1_WORDS],
    ['C1', C1_WORDS_EXTRA],
    ['B2', B2_WORDS],
    ['B2', B2_WORDS_EXTRA],
    ['B1', B1_WORDS],
    ['B1', B1_WORDS_EXTRA],
    ['A2', A2_WORDS],
    ['A2', A2_WORDS_EXTRA],
    ['A1', A1_WORDS],
];

const CEFR_RANK: Record<CefrLevel, number> = {
    A1: 1,
    A2: 2,
    B1: 3,
    B2: 4,
    C1: 5,
    C2: 6,
};

const map: Record<string, CefrLevel> = {};
for (const [level, words] of LEVELS) {
    for (const word of words) {
        const existing = map[word];
        if (!existing || CEFR_RANK[level] > CEFR_RANK[existing]) {
            map[word] = level;
        }
    }
}

export const OPEN_CEFR_VOCABULARY: Record<string, CefrLevel> = map;
